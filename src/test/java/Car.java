public class Car {

    int horsepower;
    String color;
    String fuelType;


    public static void main(String[] args) {
        Car car = new Car();
        car.horsepower = 200;
        car.color = "blue";
        car.fuelType = "diesel";

        System.out.println(car.horsepower);
        System.out.println(car.color);
        System.out.println(car.fuelType);

    }
}


